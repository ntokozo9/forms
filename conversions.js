//convert 100 pounds to kg
const convertPoundsToKilograms = (pounds) => {
  return pounds * 0.454;
};

console.log(convertPoundsToKilograms(100));

//convert 100 inches to cm
const convertInchesToCentimeters = (inches) => {
  return inches * 2.54;
};
console.log(convertInchesToCentimeters(100));

//convert 100 farhenheit to celcius
const convertFarhenheitTocelcius = (farhenheit) => {
  return (farhenheit-32) *5/9;
};
console.log(convertFarhenheitTocelcius(100));

//convert 100 feet to meters
const convertFeetToMeters = (feet) => {
  return feet * 0.305;
};
console.log(convertFeetToMeters(100));
